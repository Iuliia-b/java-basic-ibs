public class Variables {
    public static void main(String[] args) {
        double doubleVar = 11.99d;
        byte byteMax = 127;

        System.out.println("Произвольное дробное значение переменной типа с плавающей точкой с наибольшим диапазоном чисел: " + doubleVar);
        System.out.println("Hаибольшее возможное значение переменной целочисленного типа с наименьшим диапазоном чисел: " + byteMax);
    }
}
